a = 7
b = 2

#덧셈
print('{}+{} = {}'.format(a,b,a+b))
#뺄셈
print('{}-{} = {}'.format(a,b,a-b))
#곱셈
print('{}*{} = {}'.format(a,b,a*b))
#거듭제곱
print('{}**{} = {}'.format(a,b,a**b))
#나누기
print('{}/{} = {}'.format(a,b,a/b))
#몫
print('{}//{} = {}'.format(a,b,a//b))
#나머지
print('{}%{} = {}'.format(a,b,a%b))
